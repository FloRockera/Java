package fr.operateurs.console;

public class OperateursApp {

	public static void main(String[] args) {
		int a = 10;
		long b= 2000;

		System.out.println(a+"+"+b+"="+(a+b));
		System.out.println(a+"-"+b+"="+(a-b));
		System.out.println(a+"*"+b+"="+(a*b));
		System.out.println(a+"/"+b+"="+(a/b));
		System.out.println(a+"%"+b+"="+(a%b));


	}

}
